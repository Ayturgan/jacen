import re

# Словарь паттернов (всегда в нижнем регистре)
FAQ_DATA = {
    r"кто ты|как зовут|имя": 
        "У человека нет имени. Человек лишь инструмент в руках Многоликого. Некоторые звали этого человека Якен Хгар.",
    
    r"где мы|место|локация|город": 
        "Человек находится там, где его застала тень. Мы в Эссосе, в землях Эха Драконьей крови.",
    
    r"что делать|как играть|правила": 
        "Юноша должен говорить, а Якен будет слушать. Загляни в магический кристалл (Web App), чтобы увидеть свою суть.",
    
    r"привет|здравствуй|добрый день|хай": 
        "Человек приветствует тебя. Valar Morghulis.",
    
    r"валар моргулис|valar morghulis": 
        "Valar Dohaeris. Человек помнит старые слова.",
    
    r"спасибо|благодарю": 
        "Человек не нуждается в благодарности. Долг будет уплачен в свое время.",
}

def get_static_answer(text: str):
    """Ищет быстрый ответ, игнорируя регистр, знаки препинания и обращение 'Якен'."""
    # 1. Приводим к нижнему регистру
    clean_text = text.lower()
    
    # 2. Удаляем обращение "якен" и знаки препинания
    clean_text = re.sub(r'\bякен\b', '', clean_text)
    clean_text = re.sub(r'[^\w\s]', '', clean_text).strip()
    
    if not clean_text:
        return None

    # 3. Ищем совпадение
    for pattern, answer in FAQ_DATA.items():
        if re.search(pattern, clean_text, re.IGNORECASE):
            return answer
            
    return None
